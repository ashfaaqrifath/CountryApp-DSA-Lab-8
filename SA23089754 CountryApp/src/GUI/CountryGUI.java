    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        try{
            int code = Integer.parseInt(txtCountryCode.getText().trim());
            Country deletedCountry = countryList.delete(code);
            
            if(deletedCountry != null){
                JOptionPane.showMessageDialog(this, "Deleted: " + deletedCountry.getCountryName());
                txtCountryCode.setText("");
                txtDetails.setText("");
            }
            else{
                JOptionPane.showMessageDialog(this, "Country not found");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error deleting country");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try{
            Country c = countryList.find(Integer.parseInt(txtCountryCode.getText()));
            
            if(c != null){
                txtDetails.setText(c.getM49code() + "\n" + c.getCountryName() + "\n" + c.getAlpha2Code() + "\n" + c.getAlpha3Code());
            }
            else{
                JOptionPane.showMessageDialog(this, "Country not found");
                txtCountryCode.setText("");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Invalid details");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        try{
            String details = txtDetails.getText().trim();
            String[] parts = details.split("\\s+");
            
            if(parts.length != 4){
                JOptionPane.showMessageDialog(this, "Please enter 4 details");
                return;
            }
            
            int code = Integer.parseInt(parts[0]);
            String name = parts[1];
            String alpha2 = parts[2];
            String alpha3 = parts[3];
            
            Country newCountry = new Country(code, name, alpha2, alpha3);
            countryList.insert(newCountry);
            
            JOptionPane.showMessageDialog(this, "Country added");
            txtDetails.setText("");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error adding country");
        }
    }//GEN-LAST:event_btnAddActionPerformed

