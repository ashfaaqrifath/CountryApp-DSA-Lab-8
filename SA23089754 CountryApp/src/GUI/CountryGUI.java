    public void readData() {
        try {
            FileReader fr = new
            FileReader("C:\\Users\\sa23089754\\Desktop\\countryCodes.txt");
            BufferedReader br = new BufferedReader(fr);
            String details[];
            String line = br.readLine();
            
            while (line != null) {
                
                details = line.split(",");
                details[0].trim();
                countryList.insert(new Country(Integer.parseInt(details[0]),

                details[1].trim(),details[2].trim(),details[3].trim()));
                count++;
                line = br.readLine();
            }
            br.close();
        }
        catch (IOException e1) {
            System.out.println("Error in File reading!!!");
        }
        catch (Exception e2) {
            System.out.println("Error in processing!!!");
        }
    }

